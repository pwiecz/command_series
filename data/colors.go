var RGBPalette [256]color.RGBA

func clamp(v, min, max int) int{
	if v < min {
		return min
	}
	if v > max {
		return max
	}
	return v
}
func init() {
	for i, val := range rgbcolors {
		switch i%3 {
		case 0: RGBPalette[i/3].R = uint8(clamp(val, 0, 255))
		case 1: RGBPalette[i/3].G = uint8(clamp(val, 0, 255))
		case 2: RGBPalette[i/3].B = uint8(clamp(val, 0, 255))
			RGBPalette[i/3].A = 255
		}
	}
}
